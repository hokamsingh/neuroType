export type Difficulty = 'easy' | 'medium' | 'hard' | 'chaos'

function shuffle<T>(arr: T[]): T[] {
  const a = [...arr]
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[a[i], a[j]] = [a[j], a[i]]
  }
  return a
}

function buildPool(difficulty: Difficulty): string[] {
  const { letters, short, medium, long, phrases } = WORD_POOLS
  if (difficulty === 'easy')   return [...letters, ...letters, ...short]
  if (difficulty === 'medium') return [...short, ...medium]
  if (difficulty === 'hard')   return [...medium, ...long, ...phrases]
  return [...short, ...medium, ...long, ...phrases]
}

export function makeWordQueue(difficulty: Difficulty) {
  let queue: string[] = []
  function refill() { queue = shuffle(buildPool(difficulty)) }
  return {
    next(): string {
      if (!queue.length) refill()
      return queue.pop()!
    }
  }
}

